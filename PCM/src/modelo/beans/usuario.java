package modelo.beans;

public class usuario {
	private String idUsuario;
	private String idRol;
	private String usuario;
	private String pass;
	private String correo;
	private String estatus;
	private String idUsuarioRegistra;
	private String fechaRegistro;
	private String idUsuarioModifica;
	private String fechaModifica;
	private String alerta;
	
	
	public String getAlerta() {
		return alerta;
	}
	public void setAlerta(String alerta) {
		this.alerta = alerta;
	}
	public String getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}
	public String getIdRol() {
		return idRol;
	}
	public void setIdRol(String idRol) {
		this.idRol = idRol;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
	public String getIdUsuarioRegistra() {
		return idUsuarioRegistra;
	}
	public void setIdUsuarioRegistra(String idUsuarioRegistra) {
		this.idUsuarioRegistra = idUsuarioRegistra;
	}
	public String getFechaRegistro() {
		return fechaRegistro;
	}
	public void setFechaRegistro(String fechaRegistro) {
		this.fechaRegistro = fechaRegistro;
	}
	public String getIdUsuarioModifica() {
		return idUsuarioModifica;
	}
	public void setIdUsuarioModifica(String idUsuarioModifica) {
		this.idUsuarioModifica = idUsuarioModifica;
	}
	public String getFechaModifica() {
		return fechaModifica;
	}
	public void setFechaModifica(String fechaModifica) {
		this.fechaModifica = fechaModifica;
	}
	
	
	
	
}
